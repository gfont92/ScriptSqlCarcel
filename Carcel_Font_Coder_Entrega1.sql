-- CREAR TABLA PRESOS
CREATE TABLE presos (
Id_Preso INT NOT NULL AUTO_INCREMENT PRIMARY KEY,
Nombre_Apellido VARCHAR(50),
DNI INT,
F_Ingreso DATE,
F_Hasta DATE,
Id_Celda INT
);

-- CREAR TABLA CELDAS
CREATE TABLE celdas (
Id_Celda INT NOT NULL AUTO_INCREMENT PRIMARY KEY,
Num_Celda INT,
Id_Zona INT,
Pabellon VARCHAR(1)
);


-- CREAR TABLA GUARDIAS 
